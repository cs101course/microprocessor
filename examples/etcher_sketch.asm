LIR0 COLOR
INC
SR0 COLOR
JMP WAIT

COLOR_DOWN:
LIR0 COLOR
DEC
SR0 COLOR
JMP WAIT

WAIT:
LDR0 0b11110000
JNZP WAIT
JMP DRAW

