JNZP UP
UP_RESUME:

LDR0 0b0010
JNZP LEFT
LEFT_RESUME:

LDR0 0b0100
JNZP DOWN
DOWN_RESUME:

LDR0 0b1000
JNZP RIGHT
RIGHT_RESUME:

LDR0 0b00010000
JNZP COLOR_UP

LDR0 0b01000000
JNZP COLOR_DOWN

DRAW:

LIR0 X
LIR1 Y
PLOT COLOR: 15

JMP START

UP:
LIR0 Y
DEC
SR0 Y
JMP UP_RESUME

DOWN:
LIR0 Y
INC
SR0 Y
JMP DOWN_RESUME

LEFT:
LIR0 X
DEC
SR0 X
JMP LEFT_RESUME

RIGHT:
LIR0 X
INC
SR0 X
JMP RIGHT_RESUME

